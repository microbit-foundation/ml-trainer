// Snapshot the Chakra default theme foundation scales into Panda token format.
// Run with `node bin/gen-chakra-tokens.mjs`; output is committed as
// src/deployment/default/chakra-tokens.ts so the OSS Panda preset matches
// Chakra exactly and stays correct once @chakra-ui/theme is removed.
import { theme } from "@chakra-ui/theme";
import { writeFileSync } from "node:fs";

// Wrap each leaf in Panda's { value } shape, recursing into nested ramps
// (colors.blue.500, sizes.container.sm). Leaves are string | number.
const toTokens = (obj) => {
  const out = {};
  for (const [k, v] of Object.entries(obj)) {
    // Stringify leaves: Panda types most token categories as string-valued, and
    // CSS accepts the string form of every numeric value (e.g. zIndex "1400",
    // fontWeight "600", lineHeight "1.2").
    out[k] = v !== null && typeof v === "object" ? toTokens(v) : { value: String(v) };
  }
  return out;
};

const j = (o) => JSON.stringify(o, null, 2);

const colors = toTokens(theme.colors);
const spacing = toTokens(theme.space);
const sizes = toTokens(theme.sizes);
const radii = toTokens(theme.radii);
const fontSizes = toTokens(theme.fontSizes);
const fontWeights = toTokens(theme.fontWeights);
const lineHeights = toTokens(theme.lineHeights);
const letterSpacings = toTokens(theme.letterSpacings);
const zIndex = toTokens(theme.zIndices);
const blurs = toTokens(theme.blur);
const borders = toTokens(theme.borders);
const shadows = toTokens(theme.shadows);
const durations = toTokens(theme.transition.duration);
const easings = toTokens(theme.transition.easing);

// Breakpoints are a top-level Panda config value (not tokens); drop Chakra's
// `base: 0em` which Panda derives implicitly.
const { base: _drop, ...breakpoints } = theme.breakpoints;

const header = `/**
 * (c) 2026, Micro:bit Educational Foundation and contributors
 *
 * SPDX-License-Identifier: MIT
 */
/* eslint-disable */
// GENERATED by bin/gen-chakra-tokens.mjs (snapshot of @chakra-ui/theme defaults).
// Do not edit by hand. Canonical Chakra v2 foundation values in Panda token
// format so the OSS preset matches Chakra exactly.
`;

const body = `${header}
export const colors = ${j(colors)} as const;
export const spacing = ${j(spacing)} as const;
export const sizes = ${j(sizes)} as const;
export const radii = ${j(radii)} as const;
export const fontSizes = ${j(fontSizes)} as const;
export const fontWeights = ${j(fontWeights)} as const;
export const lineHeights = ${j(lineHeights)} as const;
export const letterSpacings = ${j(letterSpacings)} as const;
export const zIndex = ${j(zIndex)} as const;
export const blurs = ${j(blurs)} as const;
export const borders = ${j(borders)} as const;
export const shadows = ${j(shadows)} as const;
export const durations = ${j(durations)} as const;
export const easings = ${j(easings)} as const;
export const breakpoints = ${j(breakpoints)} as const;
`;

const outPath = new URL(
  "../src/deployment/default/chakra-tokens.ts",
  import.meta.url
);
writeFileSync(outPath, body);
console.log("wrote", outPath.pathname);
